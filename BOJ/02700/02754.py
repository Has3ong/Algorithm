def solution():
    string = str(input())

    if string == 'A+':
        print(4.3)
    elif string == 'A0':
        print(4.0)
    elif string == 'A-':
        print(3.7)
    elif string == 'B+':
        print(3.3)
    elif string == 'B0':
        print(3.0)
    elif string == 'B-':
        print(2.7)
    elif string == 'C+':
        print(2.3)
    elif string == 'C0':
        print(2.0)
    elif string == 'C-':
        print(1.7)
    elif string == 'D+':
        print(1.3)
    elif string == 'D0':
        print(1.0)
    elif string == 'D-':
        print(0.7)
    elif string == 'F':
        print(0.0)
solution()